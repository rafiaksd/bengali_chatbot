import re 

def clean_text(text):
    
    """
    Clean a single text document by removing noise and standardizing format.
    """
    # Step 1: Remove page markers (e.g., --- Page 1 ---)
    text = re.sub(r'---\s*Page\s*\d+\s*---', '', text, flags=re.IGNORECASE)

    # Step 2: Remove irrelevant sections (e.g., contact numbers, random codes)
    text = re.sub(r'\b\d{2}\s*\d{6,}\b', '', text)  # Matches numbers like "23 169709"
    text = re.sub(r'\b\d{6,}\b', '', text)  # Matches standalone large numbers like "194785"
    text = re.sub(r'অনলাইন ব্যাচ সম্পর্কিত যেকোনো জিজ্ঞাসায়,[^\n]*\n', '', text)  # Remove contact info

    # Step 3: Remove excessive whitespace and normalize line breaks
    text = re.sub(r'\n\s*\n+', '\n', text)  # Replace multiple newlines with single
    text = re.sub(r'\s+', ' ', text)  # Replace multiple spaces with single space
    text = text.strip()

    # Step 4: Remove stray symbols and formatting artifacts
    text = re.sub(r'[১২৩৪৫৬৭৮৯০]+[।&]', '', text)  # Remove numbered list markers like "৮।"
    text = re.sub(r'[১২৩৪৫৬৭৮৯০]+\s*[।]', '', text)  # Remove standalone numbered markers
    text = re.sub(r'[£€]', '', text)  # Remove stray symbols like "£97179"
    text = re.sub(r'[\|]', '', text)  # Remove stray pipes

    # Step 5: Preserve meaningful content (narrative, questions, annotations)
    # Split into lines and filter out short, irrelevant lines (e.g., single words or numbers)
    lines = text.split('\n')
    cleaned_lines = []
    for line in lines:
        # Skip lines that are too short (likely noise) or contain only symbols/numbers
        if len(line.strip()) > 10 and not re.match(r'^[\d\s\W]+$', line):
            cleaned_lines.append(line.strip())

    # Join cleaned lines
    cleaned_text = '\n'.join(cleaned_lines)
    
    # Step 6: Final normalization (remove trailing punctuation, extra spaces)
    cleaned_text = re.sub(r'\s*[।,]\s*$', '', cleaned_text, flags=re.MULTILINE)
    
    return cleaned_text

